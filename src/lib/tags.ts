export const getTagClass = (tag: string): string => {
  const known = TAG_COLORS[tag]
  if (known) return known
  let h = 0
  for (let i = 0; i < tag.length; i++) h = (h * 31 + tag.charCodeAt(i)) | 0
  return FALLBACKS[Math.abs(h) % FALLBACKS.length]
}
